package org.tierlon.schema.support;

public class FieldData {
	
	private String propertyName;
	
	// This represents the property default Value???  
	private String propertyStringValue;
	private Object propertyObjectValue;
	
	
	// ==============================================================
	// FieldData
	// ==============================================================
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	public String getPropertyStringValue() {
		return propertyStringValue;
	}
	public void setPropertyStringValue(String propertyStringValue) {
		this.propertyStringValue = propertyStringValue;
	}
	public Object getPropertyObjectValue() {
		return propertyObjectValue;
	}
	public void setPropertyObjectValue(Object propertyObjectValue) {
		this.propertyObjectValue = propertyObjectValue;
	}
	
	
}
